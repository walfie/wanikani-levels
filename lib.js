function isKanji(char) {
  return /^[\u4e00-\u9faf]+$/.test(char);
}

function getKanji(string) {
  var chars = string.split('');

  return chars.reduce(function(set, char) {
    if (isKanji(char)) { set.add(char); };
    return set;
  }, new Set());
}

function getKanjiLevels(string) {
  return Array.from(getKanji(string)).reduce(function(map, kanji) {
    var level = kanjiLevels[kanji] || null;

    var kanjiForLevel = map.get(level) || [];
    kanjiForLevel.push(kanji);

    map.set(level, kanjiForLevel);

    return map;
  }, new Map());
}

